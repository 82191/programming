#include <stdio.h>

//출석률 = (수업시간 - 결석시간) / 수업시간 * 100
//출석점수 = 출석률 * 15점

int main()
{
	printf("김은지의 출석점수 계산\n");
	printf("-----------------------\n");
	printf("수업시간    %4d\n", 90);
	printf("결석시간    %4d\n", 2);
	printf("출석률      %.1f %%\n", (58.0 / 60.0) * 100);
	printf("출석점수    %4.1f점\n", (60 - 2) / 60.0 * 15);
}
